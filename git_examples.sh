1. branches
	- git branch <branch_name>
		- create a new branch (create only)
	- git checkout <branch_name>
		- move to another branch
	- git btanches -a
		- list all branches (including remote and local branches)
	- git branch -r
		- list remote branch
2. information
	- git status
		- check your branch, file information.. etc

